            <h2>Ingredients</h2>
            <hr>

            <ul style="font-size: 20px;" class="text-justify font-weight-bold">
                <li>1000 g (7¾ cups/100%) all-purpose flour</li>
                <li>720 g (3⅛ cups/72%) water (90°F to 95°F)</li>
                <li>21 g (1 Tbsp + 1 scant tsp/2.1%) fine sea salt</li>
                <li>4 g (1 tsp/0.4%) instant dried yeast</li>
                
                </ol>

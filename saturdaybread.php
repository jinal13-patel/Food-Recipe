        <div class=" mt-5 container" id="jump">
            <h2>Preparation</h2>
            <hr>
            <p style="font-size: 20px;" class="text-justify">Autolyse: Combine the 1,000 grams of flour with the 720 grams of 90°F to 95°F (32°C to 35°C) water in a 12-quart round tub or similar container. Mix by hand just until incorporated. Cover and let rest for 20 to 30 minutes.</p>
            <p style="font-size: 20px;" class="text-justify">Mix: Sprinkle the 21 grams of salt and the 4 grams (1 level teaspoon) of yeast evenly over the top of the dough. Mix by hand, wetting your working hand before mixing so the dough doesn’t stick to you. (It’s fine to rewet your hand three or four times while you mix.)</p>
            <p style="font-size: 20px;" class="text-justify">Reach underneath the dough and grab about one-quarter of it. Gently stretch this section of dough and fold it over the top to the other side of the dough. Repeat three more times with the remaining dough, until the salt and yeast are fully enclosed.</p>
            <p style="font-size: 20px;" class="text-justify">Use the pincer method to fully integrate the ingredients. Make five or six pincer cuts across the entire mass of dough. Then fold the dough over itself a few times. Repeat, alternately cutting and folding until all of the ingredients are fully integrated and the dough has some tension in it. Let the dough rest for a few minutes, then fold for another 30 seconds or until the dough tightens up. The whole process should take about 5 minutes. The target dough temperature at the end of the mix is 77°F to 78°F (25°C to 26°C). If the final mix temperature is cooler, don’t worry, it will just take longer to fully rise (in this case tripling in size). If you have a warm spot where the dough can rise, that will help make up for the cooler dough temperature. If your dough is warmer, the dough will triple in size sooner. (The next time you make the recipe, you can adjust the final mix temperature by using warmer or cooler water.) Cover the tub and let the dough rise. </p>
            <p style="font-size: 20px;" class="text-justify">Fold: This dough needs two folds. It’s easiest to apply the folds during the first 1½ hours after mixing the dough. Apply the first fold about 10 minutes after mixing and the second fold during the next hour (when you see the dough spread out in the tub, it’s ready for the second fold). If need be, it’s okay to fold later; just be sure to leave it alone for the last hour of rising.</p>
            <p style="font-size: 20px;" class="text-justify">Remove the Dutch oven and carefully tilt it to turn the loaf out. Let cool on a rack or set the loaf on its side so air can circulate around it. Let the loaf rest for at least 20 minutes before slicing.</p>

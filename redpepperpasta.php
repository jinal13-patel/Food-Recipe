                <li>4 garlic cloves, finely grated, divided</li>
                <li>4 garlic cloves, finely grated, divided</li>
                <li>½ cup coarsely chopped parsley</li>
                <li>1 lb. ziti, rigatoni, cavatelli, or other medium tube pasta</li>
                <li>1 medium onion, finely chopped</li>
                <li>4 whole roasted red peppers from a jar (about one 16-oz. jar), finely chopped</li>
                <li>2 Tbsp. tomato paste</li>
                <li>1 (15-oz.) can white beans (such as navy or cannellini), rinsed</li>
                <li>2 tsp. finely grated lemon zest</li>
                <li>1 Tbsp. fresh lemon juice</li>

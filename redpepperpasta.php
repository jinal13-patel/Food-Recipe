            <h2>Preparation</h2>
            <hr>
            <p style="font-size: 20px;" class="text-justify">Heat 3 Tbsp. oil in a large Dutch oven or other heavy pot
                over medium-high. Add breadcrumbs, half of the garlic, and ½ tsp. salt and cook, stirring constantly to
                incorporate the garlic, until golden brown and crisp, about 5 minutes. Transfer to a small bowl and stir
                in parsley. Wipe out and reserve pot.</p>
            <p style="font-size: 20px;" class="text-justify">Meanwhile, cook pasta in a large pot of boiling salted
                water, stirring occasionally, according to package directions. Drain, reserving 2 cups pasta cooking
                liquid.</p>
            <p style="font-size: 20px;" class="text-justify">Heat 3 Tbsp. oil in reserved pot over medium-high. Cook
                onion, stirring occasionally with a wooden spoon, until slightly softened, 6–8 minutes. Add roasted red
                peppers, remaining garlic, and 1 tsp. salt. Cook, stirring often, until any liquid is evaporated and
                vegetables are combined and glossy looking, about 5 minutes. Add tomato paste and cook, stirring
                occasionally, until vegetables are coated and paste is slightly darkened in color, about 5 minutes.</p>
            <p style="font-size: 20px;" class="text-justify">Add beans, lemon zest, lemon juice, and red pepper flakes
                to pot; season with black pepper. Cook, smashing beans and stirring to combine with spoon, about 2
                minutes. Reduce heat to medium and gradually add reserved pasta cooking liquid (you might not need all
                of it), stirring and mashing as you go, until combined and sauce is thick. Remove from heat and
                immediately add pasta; toss to coat.</p>
            <p style="font-size: 20px;" class="text-justify">Divide pasta among bowls; drizzle with a little oil and
                spoon breadcrumbs on top. Squeeze some lemon juice over if desired.</p>


                    <hr style="height: 1px;" class="bg-info w-75">
                    <p style="font-size: 17px;"><b>NOTE:</b> This recipe makes one single serving. You can prepare
                        several single-serving batches in canning jars and refrigerate them for quick breakfasts through
                        the week. The oats soaking in their liquid will keep for up to 5 days, refrigerated. To make a
                        big batch of overnight oats: Scale up the ingredients to the number of servings you'd like to
                        make and combine in a large container. Refrigerate overnight, stirring a few times whenever you
                        remember. The next morning, transfer the oats and liquid to a saucepan. Place over medium-low
                        heat and simmer until creamy and cooked through.</p>
                </div>
                <div class="ml-5 mt-5">
                    <h3 style="width: 576px;">Ingredients</h3>
                    <hr style="height: 1px;" class="bg-primary w-25">
                    <ul style=" font-size: 17px;" class="container text-justify">
                        <li>1/4 cup steel-cut oats or old-fashioned rolled oats (not instant oats; use gluten-free oats
                            if needed)</li>
                        <li>1/2 to 3/4 cup water, non-dairy milk, or milk</li>
                        <li>Pinch salt</li>
                    </ul>
                    <div>
                        <p class="mt-5">
                            <b style="font-size: 18px;">To Serve- Choose a few!</b>
                        <ul style="font-size: 17px;" class="container text-justify">
                            <li>Brown sugar, maple syrup, honey, or other sweetener</li>
                            <li>Dried fruit, like dried cherries, cranberries, or figs</li>
                            <li>Fresh fruit, like raspberries, blue berries, bananas, apples, or pears</li>
                            <li>Chopped nuts, like pecans, pistachios, walnuts, or almonds</li>
                            <li>Splash of milk, creme fraiche, or yogurt</li>
                        </ul>
                        </p>
                        <div class="container ml-5 mt-5">
                            <h3 style="width: 576px;">Method</h3>
                            <hr style="height: 1px;" class="bg-primary w-25">
                            <ol style="font-size: 18px;" class="text-justify">
                                <li style="width: 75%;">
                                    Combine the oats, water, and salt in a jar:
                                    Use 1/2 cup of water for thick, spoonable oatmeal, or 3/4 cup for a softer, more
                                    porridge-like oatmeal. (You can also stir in additional liquid later on to thin it
                                    out, so
                                    no need to add too much here.) Secure the lid and swirl the contents a few times so
                                    that the
                                    oats are soaked.
                                </li>
                                <li style="width: 75%;">
                                    Pack up your toppings:
                                    Combine a handful of chosen oatmeal toppings in a small container. (If you're making
                                    multiple servings for breakfasts during the week, wait to assemble the toppings
                                    until the
                                    night before you plan to eat the oatmeal.)
                                </li>
                                <li style="width: 75%;">
                                    Refrigerate overnight or up to 5 days.
                                </li>
                                <li style="width: 75%;">
                                    Microwave the oatmeal:
                                    Remove the lid from the container and set aside. If desired, transfer the oats and
                                    their
                                    soaking liquid to a separate microwave-safe bowl. Place the container on a
                                    microwave-safe
                                    plate (to catch any accidental spills), and place in the microwave.
                                    <br>
                                    Microwave on high power for 30 seconds, then remove the oatmeal and stir.
                                    Microwave for
                                    another 30 seconds, then remove and stir again. If the liquid is not quite
                                    piping hot,
                                    microwave for another 15 to 30 seconds, or as needed until the liquid is very
                                    hot. Cooking
                                    time may vary from microwave to microwave; in my microwave, 1 minute and 20
                                    seconds total
                                    cooking time was perfect.
                                </li>
                                <li style="width: 75%;">
                                    Be very watchful when microwaving longer than 1 minute. The liquid will start to
                                    foam up and out of the container if it starts to boil. Be especially careful with
                                    smaller containers.
                                    <br>
                                    At this point, the oats probably won't quite look like oatmeal yet; there will still
                                    be a thin layer of foamy liquid on top of the oats. This is ok.

                                </li>
                                <img src="food_recipe_css/oats1.jpg" class="mt-5" style="margin-left: 200px;" height="30%"
                                    width="35%" alt="">
                                <li class="mt-5" style="width: 75%;">Let the oats stand for 2 to 3 minutes to thicken:
                                    After letting them stand a few minutes, give the oats a stir. The oatmeal should
                                    become thick and creamy as you stir. If it still seems more liquidy than you'd like,
                                    microwave for another 15 to 30 seconds, but be very watchful for foaming liquid.

                                    If the oats seem too stiff and thick, stir in splash of milk, water, or other liquid
                                    to loosen them up again.
                                </li>
                                <div class="row mt-5">
                                    <img style="margin-right: 30px; margin-left: 100px;" class="w-25 h-25" src="food_recipe_css/oats2.jpg" alt="">
                                    <img src="food_recipe_css/oats3.jfif" alt="">
                                </div>
                                <li style="width: 75%;">
                                    Stir in toppings and serve.
                                </li>
                                <p class="mt-2">Enjoy!</p>
                            </ol>

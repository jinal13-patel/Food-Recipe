        <div class=" mt-5 container">
            <h2>Why Overnight Oats Are So Good</h2>
            <hr>
            <p style="font-size: 20px;" class="text-justify">Overnight soaking gives the oats time to absorb their
                cooking liquid, whether that's water or milk. Since they're already fully saturated with liquid, all you
                really need to do is heat them up.</p>
            <p style="font-size: 20px;" class="text-justify">In fact, you don't even need to heat them up if you don't
                want to; after soaking, the raw grains are delightfully chewy and great as a cold breakfast on a hot
                day.</p>
            <p style="font-size: 20px;" class="text-justify">I also feel that overnight soaking helps the grains cook
                more evenly, leading to the creamy, smooth texture that made me fall in love with my bowl of oatmeal at
                the Brick Oven.</p>
        </div>
        <div class="mt-5 container">
            <h2>How to Make the Best Overnight Oats</h2>
            <hr>
            <p style="font-size: 20px;" class="text-justify">Overnight oats are nothing new on the Internet, and I'm
                certainly not alone in my enthusiasm for them. Do a quick search on Pinterest and you'll find enough
                oatmeal inspiration to last you through a decade of breakfasts.</p>
            <p style="font-size: 20px;" class="text-justify">To settle on the very best method, I tested the oats with a
                variety of different oat-to-water ratios, with different liquids in place of the water, with both
                steel-cut and regular old-fashioned rolled oats, and after different soaking times.</p>
            <p style="font-size: 20px;" class="text-justify">In the end, I decided that the simplest method was also the
                best:
            </p>
            <ol style="font-size: 20px;" class="text-justify font-weight-bold">
                <li>Ratio of 1/4 cup oats (steel cut or old-fashioned) to 1/2 cup water</li>
                <li>Overnight soak</li>
                <li>About a minute in the microwave</li>
            </ol>
        </div>
        <div class=" mt-5 container">
            <h2>Best Toppings for Overnight Oats</h2>
            <hr>
            <p style="font-size: 20px;" class="text-justify">In my opinion, oatmeal is 99% about the delicious toppings
                stir into your bowl. Here are some favorite suggestions:</p>
            <ul style="font-size: 20px;" class="text-justify">
                <li><b>Sweeteners:</b> Brown sugar, maple syrup, honey, or other sweetener</li>
                <li><b>Dried fruit</b>, like dried cherries, cranberries, or figs</li>
                <li><b>Fresh fruit</b>, like raspberries, blue berries, bananas, apples, or pears</li>
                <li><b>Chopped nuts</b>, like pecans, pistachios, walnuts, or almonds</li>
                <li><b>Splash of milk, cream, creme fraiche, or yogurt</b></li>
            </ul>

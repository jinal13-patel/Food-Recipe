            <h2>Ingredients</h2>
            <hr>

            <ul style="font-size: 20px;" class="text-justify font-weight-bold">
                <li>32 green cardamom pods, coarsely crushed in a mortar and pestle</li>
                <li>1 tsp. ground ginger</li>
                <li>1 tsp. whole black peppercorn, coarsely crushed in a mortar and pestle</li>
                <li>2 Tbsp. CTC Assam tea (such as Red Label, Tea India, 24 Mantra Organic, or Wagh Bakri)</li>
                <li>3 cups milk, dairy or plant-based</li>
                <li>4 tsp. granulated sugar or jaggery, divided, or to taste.</li>

                </ol>
        </div>
        <div class=" mt-5 container" id="jump">
            <h2>Preparation</h2>
            <hr>
            <p style="font-size: 20px;" class="text-justify">To a 5-qt pot, add 3 cups water, cardamom pods and seeds,
                ginger, and pepper and bring to a rolling boil over high heat. Then add tea and boil for 4–5 minutes
                until the water looks darker..</p>
            <p style="font-size: 20px;" class="text-justify">Add milk and let boil until the foam almost overflows the
                pot. (Not all plant-based milk will rise when it boils. If yours doesn’t, keep it at a rolling boil for
                30 seconds before continuing with the recipe.) Place a wooden spoon across the pot to avoid bubbling
                over, and lower heat briefly. When foam has settled, return to medium heat, continuing to boil for a
                total of 10 minutes from the time that milk was added, until the liquid has reduced by ⅓ and has a deep
                tan color. If the foam continues to rise, lower heat slightly.</p>
            <p style="font-size: 20px;" class="text-justify">Remove from heat. Using a strainer, strain tea into 4
                teacups. (For a frothy chai, pour the chai from one cup to another a few times until bubbles form.) Add
                about 1 tsp. sugar (or more to taste) per cup, and serve immediately.</p>
            <p style="font-size: 20px;" class="text-justify">Swap or add in one or all of these spices to make a
                different cup of chai: 2 tsp. dried mint, 8 cloves (coarsely crushed), one 4-inch cinnamon stick
                (crushed into smaller pieces).</p>
            

